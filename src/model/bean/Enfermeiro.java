package model.bean;


public class Enfermeiro {
    private int Id_enfermeiro;
    private String nome;
    private String especialização;
    private String login;
    private String senha;
    private String email;
    private String endereco;
    private String tipo;
    private String pesquisar;

    /**
     * @return the Id_enfermeiro
     */
    public int getId_enfermeiro() {
        return Id_enfermeiro;
    }

    /**
     * @param Id_enfermeiro the Id_enfermeiro to set
     */
    public void setId_enfermeiro(int Id_enfermeiro) {
        this.Id_enfermeiro = Id_enfermeiro;
    }

    /**
     * @return the nome
     */
    public String getNome() {
        return nome;
    }

    /**
     * @param nome the nome to set
     */
    public void setNome(String nome) {
        this.nome = nome;
    }

    /**
     * @return the especialização
     */
    public String getEspecialização() {
        return especialização;
    }

    /**
     * @param especialização the especialização to set
     */
    public void setEspecialização(String especialização) {
        this.especialização = especialização;
    }

    /**
     * @return the login
     */
    public String getLogin() {
        return login;
    }

    /**
     * @param login the login to set
     */
    public void setLogin(String login) {
        this.login = login;
    }

    /**
     * @return the senha
     */
    public String getSenha() {
        return senha;
    }

    /**
     * @param senha the senha to set
     */
    public void setSenha(String senha) {
        this.senha = senha;
    }

    /**
     * @return the email
     */
    public String getEmail() {
        return email;
    }

    /**
     * @param email the email to set
     */
    public void setEmail(String email) {
        this.email = email;
    }

    /**
     * @return the endereco
     */
    public String getEndereco() {
        return endereco;
    }

    /**
     * @param endereco the endereco to set
     */
    public void setEndereco(String endereco) {
        this.endereco = endereco;
    }

    /**
     * @return the tipo
     */
    public String getTipo() {
        return tipo;
    }

    /**
     * @param tipo the tipo to set
     */
    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    /**
     * @return the pesquisar
     */
    public String getPesquisar() {
        return pesquisar;
    }

    /**
     * @param pesquisar the pesquisar to set
     */
    public void setPesquisar(String pesquisar) {
        this.pesquisar = pesquisar;
    }
}
