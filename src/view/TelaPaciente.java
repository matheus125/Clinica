    private void btnCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelarActionPerformed
        int resposta = 0;
            resposta = JOptionPane.showConfirmDialog(rootPane, "Deseja Cancelar o Cadastro?");
            if (resposta == JOptionPane.YES_OPTION) { 
                LimparCampos();
                DesabiliarCampos();
                DesabilitarBotões();
                btnNovo.setEnabled(true);
            }
    }//GEN-LAST:event_btnCancelarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        flag=2;
        HabilitarCampos();
        btnSalvar.setEnabled(true);
        btnCancelar.setEnabled(true);
        btnEditar.setEnabled(false);
        btnExcluir.setEnabled(false);
        btnNovo.setEnabled(false);
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        pac.setPesquisar(txtPesquisar.getText());
        Paciente model = dao.buscarPaciente(pac);
        txtID_Paciente.setText((String.valueOf(model.getId_paciente())));
        txtNomePac.setText(model.getNome());
        txtNascimentoPac.setText(model.getNascimento());
        txtRGPac.setText(model.getRg());
        txtfonePac.setText(model.getTelefone());
        txtRuaPac.setText(model.getRua());
        txtCepPac.setText(model.getCep());
        ComboBairro.setSelectedItem(model.getNomebairro());
        txtComplementoPac.setText(model.getComplemento());
        btnEditar.setEnabled(true);
        btnExcluir.setEnabled(true);
        txtPesquisar.setText("");
    }//GEN-LAST:event_btnBuscarActionPerformed

