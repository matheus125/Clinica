    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if (txtNomePac.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Informe o Nome para cadastrar!");
            txtNomePac.requestFocus();
        }else if (txtNascimentoPac.getText().equals("  /  /    ")) {
            JOptionPane.showMessageDialog(null, "Informe a sua data de nascimento!");
            txtNascimentoPac.requestFocus();
        }else if (txtRGPac.getText().equals("       - ")) {
            JOptionPane.showMessageDialog(null, "Informe o Seu RG!");
            txtRGPac.requestFocus();
        }else if (txtfonePac.equals("(  )     -    ")) {
            JOptionPane.showMessageDialog(null, "Informe o número de telefone!");
            txtfonePac.requestFocus();
        }else if (txtRuaPac.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Informe o nome da sua Rua!");
            txtRuaPac.requestFocus();
        }else if (txtCepPac.getText().equals("     -   ")) {
            JOptionPane.showMessageDialog(null, "Confirme o seu CEP!");
            txtCepPac.requestFocus();
        }else if(flag==1){
        pac.setNome(txtNomePac.getText());
        pac.setNascimento(txtNascimentoPac.getText());
        pac.setRg(txtRGPac.getText());
        pac.setTelefone(txtfonePac.getText());
        pac.setRua(txtRuaPac.getText());
        pac.setCep(txtCepPac.getText());
        pac.setNomebairro((String) ComboBairro.getSelectedItem());
        pac.setComplemento(txtComplementoPac.getText());
        dao.Salvar(pac);
        int resposta = 0;
            resposta = JOptionPane.showConfirmDialog(rootPane, "Deseja fazer mais alguma coisa?");
            if (resposta == JOptionPane.NO_OPTION) { 
                dispose();
            }
        LimparCampos();
        DesabilitarBotões();
        DesabiliarCampos();
        btnNovo.setEnabled(true); 
        }else{
        pac.setId_paciente(Integer.parseInt(txtID_Paciente.getText()));
        pac.setNome(txtNomePac.getText());
        pac.setNascimento(txtNascimentoPac.getText());
        pac.setRg(txtRGPac.getText());
        pac.setTelefone(txtfonePac.getText());
        pac.setRua(txtRuaPac.getText());
        pac.setCep(txtCepPac.getText());
        pac.setNomebairro((String) ComboBairro.getSelectedItem());
        pac.setComplemento(txtComplementoPac.getText());
        dao.Editar(pac);
        int resposta = 0;
            resposta = JOptionPane.showConfirmDialog(rootPane, "Deseja fazer mais alguma coisa?");
            if (resposta == JOptionPane.NO_OPTION) { 
                dispose();
            }
        LimparCampos();
        DesabilitarBotões();
        DesabiliarCampos();
        btnNovo.setEnabled(true);   
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

