    public void preencherBairros(){
        con.getConnection();
        con.executaSql("select * from bairro order by nomebairro");
        try {
            con.rs.first();
            ComboBairro.removeAllItems();
            do {
                ComboBairro.addItem(con.rs.getString("nomebairro"));
            } while (con.rs.next());
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao preencher o bairro"+ex.getMessage());
        }
        con.desconecta();
    }
    
